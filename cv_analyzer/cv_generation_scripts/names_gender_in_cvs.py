import pandas as pd
import io

def split_names_gender_and_export(text_data, output_file="names_gender_split.csv"):
    """
    Splits first and last names and gender from a text string and exports to CSV or Excel.

    Args:
        text_data (str): The text data containing names and gender, one per line.
        output_file (str): The name of the output file (e.g., "names.csv" or "names.xlsx").
    """
    lines = text_data.strip().split('\n')
    data = []
    for line in lines:
        parts = line.split(maxsplit=2)  # Split into at most 3 parts
        if len(parts) == 3:
            first_name, last_name, gender = parts
            data.append({"First Name": first_name, "Last Name": last_name, "Gender": gender})
        elif len(parts) == 2:
            first_name, gender = parts
            data.append({"First Name": first_name, "Last Name": None, "Gender": gender})
        else:
          print(f"Error processing line: {line}")

    df = pd.DataFrame(data)

    if output_file.endswith(".csv"):
        df.to_csv(output_file, index=False)
        print(f"Data exported to {output_file}")
    elif output_file.endswith(".xlsx"):
        df.to_excel(output_file, index=False)
        print(f"Data exported to {output_file}")
    else:
        print("Invalid output file format. Please use .csv or .xlsx.")

